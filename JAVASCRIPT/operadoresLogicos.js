// comparador lógico AND - &&
console.log(10 > 5 && 8 > 10); // TODAS tem que ser VERDADDE pra retornar true

// comparador lógico OR - ||
console.log(10 > 5 || 8 > 10); // apenas UMA sendo verdade, já retorna true pq é um OU outro

// comparador de negação
console.log(!true) // Inverte o valor. É isso! se aglo é VERDADE, retorna false e se algo é FALSO, retorna true

// operador de concatenar
let nome = "Joao";
let sobrenome = "Oliveira";
console.log(nome + sobrenome);
console.log(nome + " " + sobrenome);
