// string usando aspas duplas

let nome = "Vinicius";
console.log(nome);

// string usando aspas simples
let sobrenome = 'Clunc';
console.log(sobrenome);

// inteiro
let idade = 22;
console.log(idade);

// decimal
let altura = 1.81;
console.log(altura);

// booleano true
let diaChuvoso = true;
console.log(diaChuvoso);

// booleano false
let vouPraPraia = false;
console.log(vouPraPraia);

// Valor Not a Number ou NaN
let naoNumerico = NaN;
console.log(naoNumerico);

// Valores Null
let jogadores = null;
console.log(jogadores);

// undefined
let espectarores = undefined;
espectarores = 10;
console.log(espectarores);

// Obejto Literal
let moto = {
    marca: "Honda",
    ano: "2019",
    modelo: "CB500"
}
console.log(moto);

// Array
let series = ["Peaky Blinders", "Breaking Bad", "Ozark"];
console.log(series);