let data = new Date()

// getDay() retorna o dia da semana que começa no Domingo com 0(zero)
console.log(data.getDay())

// getMonth() retorna o mês que começa com Jan igual a 0(zero)
console.log(data.getMonth())

// getFullYera() retorna o ano
console.log(data.getFullYear())

// getDate() retorna de hoje
console.log(data.getDate())